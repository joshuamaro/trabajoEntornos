    public void introducirDatos(){
        jTextField1.setText(hab.getTipoHab());
        jTextField2.setText(hab.getCamas()+"");
        jTextField3.setText(hab.getTipoCama());
        jTextField4.setText(hab.getTipoBaño());
        jTextField5.setText(hab.getNota()+"");
        jTextField6.setText(hab.getPrecio()+"");
        ImageIcon[] img = new ImageIcon[hab.getNImg()];
        img[0]=new ImageIcon(new ImageIcon(hab.getImagenes(0)).getImage().getScaledInstance(350, 450, java.awt.Image.SCALE_SMOOTH));
        jLabel7.setIcon(img[0]);
        for (int i=1; i<hab.getNImg(); i++){
            img[i] = new ImageIcon(new ImageIcon(hab.getImagenes(i)).getImage().getScaledInstance(200, 200, java.awt.Image.SCALE_SMOOTH));
        }
        JLabel[] jlab = new JLabel[2];
        jlab[0]=jLabel8;
        jlab[1]=jLabel9;
        for (int i=1; i<img.length; i++){
            jlab[i-1].setIcon(img[i]);
        }
    }
    
    

